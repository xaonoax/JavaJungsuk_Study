package Ch03_Operation;

public class Ch03_ex3_06 {

	public static void main(String[] args) {
		int a = 10;
		int b = 4;
		
		System.out.printf("%d + %d = %d%n", a, b, a + b);
		System.out.printf("%d + %d = %d%n", a, b, a - b);
		System.out.printf("%d + %d = %d%n", a, b, a * b);
		System.out.printf("%d + %d = %d%n", a, b, a / b);
		System.out.printf("%d + %f = %f%n", a, (float)b, a / (float)b);
	}

}
